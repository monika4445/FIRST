let arr = [44, 23, -7, -2, 10, 11]; 
let positive = 0, odd = 0;
for(let i = 0; i < arr.length; ++i){
    if(arr[i] >= 0){
        ++positive;
    }
    if(arr[i] % 2 != 0){
        ++odd;
    }
}

console.log("The Quantity of Positive elements: " + positive);
console.log("The Quantity of odd elements: " + odd);





























































